let idade = 21;

if (idade > 17) {
  console.log("você é maior de idade");
} else {
  console.log("você é menor de idade");
}

// == e ===
if (idade == 21) {
  console.log("idade é igual a 21");
  // == somente se os dados são iguais
}
if (idade === 21) {
  console.log("idade é igual a 21");
  // === verificar se o dado é igual e o seu tipo também.
}
